try:
    r = int(input("원의 반지름을 정수로 입력해주십시오:"))
    
except:
    print("입력값이 정수가 아닙니다.")

else:
    print("원의반지름:", r)
    print("원의둘레:", 2*π*r)
    print("원의넓이:", π*r**2)

