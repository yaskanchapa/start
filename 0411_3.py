# 예외의 상황에도 어떻게 대처 하라고 프로그래밍을 해줘야 한다.
# 그런대 if문을 사용해서 예외를 다 지정해주기면 뭐가 어떻게 일어날 지 전부 예상하긴 힘들다.
# 그래서 사용하는게 try except 구문이다.
# try 안의 구문은 예외가 일어 날 수 있는 구문이고 예외가 일어날 시에는 except()구문쪽을 실행 하란 의미.

